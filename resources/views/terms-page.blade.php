                <h2 class="text-lg font-semibold text-white mb-3">5. Créditos y pagos</h2>
                <p class="mb-3">Los créditos se compran por adelantado a través de Stripe, nuestro procesador de pagos certificado PCI DSS. Un crédito equivale a un intento de llamada.</p>
                <ul class="list-disc pl-5 space-y-2">
                    <li>Una llamada fallida (no contestada, ocupada, buzón de voz, error del operador) se reembolsa automáticamente al saldo del usuario.</li>
                    <li>Una llamada completada (contestada y procesada) consume el crédito, independientemente de la duración o resultado de la conversación.</li>
                    <li>Los créditos comprados no son reembolsables en efectivo ni transferibles a otros usuarios.</li>
                    <li>Los créditos no tienen fecha de vencimiento mientras la cuenta esté activa.</li>
                </ul>
                <p class="mt-3">Vacilada se reserva el derecho de ajustar precios, planes y cuotas gratuitas en cualquier momento. Los cambios se publicarán en el sitio con al menos 7 días de anticipación y no afectarán créditos ya comprados.</p>
            </section>

            <section class="bg-matrix-800 border border-matrix-600 rounded-xl p-6">
                <h2 class="text-lg font-semibold text-white mb-3">6. Programa de referidos</h2>
