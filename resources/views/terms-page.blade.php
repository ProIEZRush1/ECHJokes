                <h2 class="text-lg font-semibold text-white mb-3">2. Aceptación del contrato</h2>
                <p>Al crear una cuenta, comprar créditos o enviar una llamada, el usuario declara tener al menos 18 años y aceptar estos términos en su totalidad. Si no estás de acuerdo con cualquier parte de estos términos, no uses el servicio.</p>
