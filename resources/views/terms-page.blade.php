            <section class="bg-matrix-800 border border-matrix-600 rounded-xl p-6">
                <h2 class="text-lg font-semibold text-white mb-3">3. Uso responsable</h2>
                <p class="mb-3">El usuario es el único responsable del contenido que describe y del consentimiento implícito o explícito de la persona que recibe la llamada. Al enviar una broma, el usuario declara que:</p>
                <ul class="list-disc pl-5 space-y-2">
                    <li>Conoce personalmente a la persona que recibirá la llamada y tiene una relación personal con ella (amigo, familiar, compañero de trabajo).</li>
                    <li>La persona está en condiciones de recibir una broma ligera sin sufrir daño psicológico, físico o material.</li>
                    <li>No usará Vacilada para acosar, amenazar, extorsionar, engañar ni defraudar a nadie.</li>
                    <li>Asume toda la responsabilidad legal y moral por las consecuencias de la llamada.</li>
                </ul>
