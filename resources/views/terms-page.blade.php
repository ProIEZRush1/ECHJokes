                <h2 class="text-lg font-semibold text-white mb-3">1. Qué es Vacilada</h2>
                <p>Vacilada (vacilada.com) es una plataforma de entretenimiento que permite enviar llamadas telefónicas generadas con inteligencia artificial a personas que el usuario conoce, con fines de broma inocente y consensuada. El servicio lo opera el titular del dominio vacilada.com desde la Ciudad de México, México.</p>
