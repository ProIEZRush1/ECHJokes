            </section>

            <section class="bg-matrix-800 border border-matrix-600 rounded-xl p-6">
                <h2 class="text-lg font-semibold text-white mb-3">7. Propiedad intelectual</h2>
                <p>Las grabaciones y transcripciones generadas pertenecen al usuario que las creó, siempre y cuando cumpla con estos términos. Vacilada conserva una licencia limitada, no exclusiva y revocable para procesar, almacenar, reproducir y mostrar el contenido como parte del funcionamiento del servicio (incluyendo la página de trending y enlaces compartidos).</p>
            </section>

            <section class="bg-matrix-800 border border-matrix-600 rounded-xl p-6">
                <h2 class="text-lg font-semibold text-white mb-3">8. Limitación de responsabilidad</h2>
                <p>Vacilada se ofrece "tal cual" (as-is). No garantizamos disponibilidad ininterrumpida del servicio, calidad específica de audio, resultado particular de cada llamada, ni compatibilidad con todos los operadores telefónicos. La responsabilidad por cualquier consecuencia derivada de una broma — legal, social, emocional o de cualquier otra naturaleza — recae exclusivamente en el usuario que la envió.</p>
            </section>

            <section class="bg-matrix-800 border border-matrix-600 rounded-xl p-6">
