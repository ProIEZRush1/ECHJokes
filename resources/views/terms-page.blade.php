                <p>Podemos suspender temporalmente o cerrar permanentemente cuentas que violen estos términos, abusen del sistema de referidos, intenten evadir la moderación de contenido, o reciban reportes creíbles de la persona que recibió la llamada. En caso de cierre por violación de términos, los créditos restantes no serán reembolsados.</p>
