        <section>
            <h2 class="text-2xl font-bold mb-4">Nuestra historia</h2>
            <div class="bg-matrix-800 border border-matrix-600 rounded-xl p-5 space-y-3 text-gray-300 leading-relaxed">
                <p>Todo empezó con una vacilada. En serio. Un grupo de amigos en una taquería de la Condesa intentaba hacer una broma telefónica a un cuate que acababa de comprar un carro nuevo. La idea era simple: llamarle fingiendo ser del estacionamiento del centro comercial para avisarle que su auto había sido chocado. El problema fue que ninguno pudo mantener la seriedad ni dos segundos. Las carcajadas los delataron antes de la primera frase.</p>
                <p>Esa noche, entre cervezas y risas, surgió la pregunta que lo cambió todo: "¿Y si una IA pudiera hacer la llamada por nosotros?" No como un robot frío y sin gracia, sino como alguien que sonara genuinamente mexicano. Con las pausas correctas, las muletillas de siempre — "no, pues sí", "fíjate que", "mira, lo que pasa es que" — y sobre todo, con la capacidad de improvisar cuando la víctima empezara a sospechar.</p>
                <p>Las bromas telefónicas son una tradición profundamente arraigada en la cultura mexicana. Desde los programas de radio de los noventa hasta los videos virales de TikTok, las vaciladas han hecho reír a generaciones enteras. Pero había un problema real: hacer una buena broma telefónica requiere talento actoral, nervios de acero e improvisación de nivel profesional. La gran mayoría de la gente se queda con las ganas porque sabe que se le va a quebrar la voz o se va a morir de risa antes de tiempo.</p>
                <p>Vacilada nació en 2026 en Ciudad de México con una misión clara: democratizar el humor telefónico. Que cualquier persona — sin importar si tiene talento para la actuación o no — pueda hacer que un amigo, familiar o compañero de trabajo se muera de risa con una llamada perfectamente ejecutada. Tú pones la creatividad, la IA pone la voz y la actuación.</p>
            </div>
        </section>

        <!-- Nuestra misión -->
        <section>
            <h2 class="text-2xl font-bold mb-4">Nuestra misión</h2>
            <div class="bg-matrix-800 border border-matrix-600 rounded-xl p-5 space-y-3 text-gray-300 leading-relaxed">
                <p>Nuestra misión es hacer reír a México. Así de simple y así de ambicioso. Creemos firmemente que la tecnología más avanzada del mundo debería servir para algo tan humano como provocar una carcajada. En un mundo donde la Inteligencia Artificial se usa para automatizar procesos corporativos y resolver ecuaciones complicadas, nosotros la usamos para algo igual de importante: conectar a las personas a través del humor.</p>
                <p>Pero ojo: la tecnología debe servir al humor, nunca reemplazarlo. La gracia de una broma telefónica no está en la voz artificial — está en el escenario absurdo que alguien se inventó pensando específicamente en su amigo. Está en conocer sus miedos, sus manías, sus puntos débiles. La IA es solo el instrumento; el comediante eres tú.</p>
                <p>Por eso cada función que construimos pasa por el mismo filtro: ¿esto va a hacer que la gente se ría más? Si la respuesta es sí, lo hacemos. Si no, no importa qué tan innovador sea — no lo necesitamos.</p>
            </div>
