        <section>
            <h2 class="text-2xl font-bold mb-4">Cómo lo hacemos</h2>
            <div class="bg-matrix-800 border border-matrix-600 rounded-xl p-5 space-y-3 text-gray-300 leading-relaxed">
                <p>Detrás de cada broma de Vacilada hay un sistema de Inteligencia Artificial diseñado específicamente para sonar como un mexicano real. No usamos voces genéricas de traductor ni acentos neutros de call center. Nuestras voces fueron creadas a partir de locutores mexicanos profesionales (con su autorización y licencia), capturando las entonaciones, ritmos y expresiones que hacen que una conversación suene auténtica.</p>
                <p>Cuando tú escribes un escenario — por ejemplo, "finge que eres un vecino quejándose del ruido a las 3 de la mañana" — nuestro motor de IA entiende el contexto, construye un personaje creíble y genera respuestas en tiempo real durante la llamada. Si la persona pregunta algo inesperado, la IA improvisa. Si alguien dice "¿eres un robot?", la IA se defiende con naturalidad: "¿Robot? No, hombre, ¿pues qué te pasa? Soy tu vecino del 4B".</p>
                <p>El sistema incluye muletillas naturales del español mexicano — "ajá", "órale", "este...", "¿verdad?" — y maneja pausas, titubeos y cambios de tono como lo haría cualquier persona en una conversación telefónica real. Toda la llamada se graba automáticamente para que puedas escucharla, compartirla por WhatsApp o subirla a tus redes sociales.</p>
                <p>Además, contamos con una capa de moderación que filtra contenido peligroso o dañino en tiempo real. La IA está entrenada para hacer reír, no para acosar, amenazar ni causar daño real a nadie.</p>
            </div>
        </section>

